# This program already contains two integer variables, x and y:
# x = 27
# y = 15
# Please complete the program so that it also prints out the following:
# 27 + 15 = 42
# 27 - 15 = 12
# 27 * 15 = 405
# 27 / 15 = 1.8

x = 27
y = 15
sum = x + y
dif = x - y
prod = x * y
quot = x / y
print(f"27 + 15 = {sum}")
print(f"27 - 15 = {dif}")
print(f"27 * 15 = {prod}")
print(f"27 / 15 = {quot}")
